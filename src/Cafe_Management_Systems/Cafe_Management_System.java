    private void btnSearchBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchBillActionPerformed
        // TODO add your handling code here:
        btnClear.setEnabled(true);
        String s = JOptionPane.showInputDialog("Enter Reference Number");
        if (s != null) {
            s = s + ".txt";
            File f = new File(s);
            FileReader fr;
            try {
                fr = new FileReader(f);
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "File Not Found!", "Error", ERROR_MESSAGE);
                btnClear.setEnabled(false);
                return;
            }

            BufferedReader br = new BufferedReader(fr);
            String line;
            try {
                while ((line = br.readLine()) != null) {
                    txtReceipt.append(line);
                    txtReceipt.append("\n");
                }
            } catch (IOException ex) {
                Logger.getLogger(Cafe_Management_System.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            btnClear.setEnabled(false);
        }

    }//GEN-LAST:event_btnSearchBillActionPerformed

